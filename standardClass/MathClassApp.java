package standardClass;

public class MathClassApp {
    public static void main(String[] args) {
    
        // selengkapnya lihat di dokumentasi math class
        // Math.cos(double) --> untuk menghitung cos di trigonometric, akan mereturn nilai dengan tipe data double
        // Math.sin(double) --> untuk menghitung sin di trigonometric
        // Math.tan(double) --> untuk menghitung tan di trigonometric
        // Math.floor(double) --> untuk pembulatan ke bawah
        // Math.ceil(double) --> untuk pembulatan ke atas
        // Math.min(number1, number2) --> mengambil nilai terkecil
        // Math.max(number1, number2) --> mengambil nilai terbesar
        // Math.abs(number) --> mengambil nilai absolute
        var value = Math.cos(30);
        System.out.println(value);
    }
}
